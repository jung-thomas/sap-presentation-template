const ACCENT_TO_PALETTE = {
  'E76500': { family: 'mango',     tint: 6 },
  '049F9A': { family: 'teal',      tint: 6 },
  '36A41D': { family: 'green',     tint: 6 },
  'FA4F96': { family: 'raspberry', tint: 6 },
  'F31DED': { family: 'pink',      tint: 6 },
  '7858FF': { family: 'indigo',    tint: 6 }
}

export const HLINK_TO_PALETTE = {
  '0070F2': { family: 'blue', tint: 7 },
  '1B90FF': { family: 'blue', tint: 6 }
  // Add more if future POTX uses different hlink hexes
}

export function emitPaletteCss({ palette, themeAccents = {}, meta = {} }) {
  const { potxHash = '(unknown)', date = '(unknown)' } = meta
  const lines = []
  lines.push('/* GENERATED by scripts/extract-brand.mjs — do not edit by hand. */')
  lines.push(`/* POTX hash: ${potxHash}  |  extracted: ${date} */`)
  lines.push('')
  lines.push(':root {')
  lines.push('  /* === Named POTX-legend tokens (canonical) === */')
  for (const c of palette) {
    lines.push(`  --sap-${c.family}-${c.tint}: #${c.hex};`)
  }
  lines.push('')
  lines.push('  /* === Paired text-on-tint tokens === */')
  for (const c of palette) {
    lines.push(`  --sap-text-on-${c.family}-${c.tint}: ${c.textOn};`)
  }
  lines.push('')
  lines.push('  /* === Theme-scheme aliases (from theme1.xml clrScheme) === */')
  for (let i = 1; i <= 6; i++) {
    const slot = `accent${i}`
    const hex = themeAccents[slot]
    const ref = hex && ACCENT_TO_PALETTE[hex.toUpperCase()]
    if (ref) {
      lines.push(`  --sap-accent-${i}: var(--sap-${ref.family}-${ref.tint});`)
    }
  }
  if (themeAccents.hlink) {
    const hlinkRef = HLINK_TO_PALETTE[themeAccents.hlink.toUpperCase()]
    if (hlinkRef) {
      lines.push(`  --sap-link: var(--sap-${hlinkRef.family}-${hlinkRef.tint});`)
    }
  }
  const hasGrey = (tint) => palette.some(c => c.family === 'grey' && c.tint === tint)
  if (hasGrey(11)) lines.push('  --sap-text-primary: var(--sap-grey-11);')
  if (hasGrey(7))  lines.push('  --sap-text-secondary: var(--sap-grey-7);')
  lines.push('')
  lines.push('  /* === Typeface tokens (see theme/styles/sap-72-fonts.css) === */')
  lines.push(`  --sap-font-family: '72', '72full', 'Inter', system-ui, -apple-system, sans-serif;`)
  lines.push(`  --sap-font-family-bold: '72-Bold', '72full', 'Inter', system-ui, sans-serif;`)
  lines.push('  --sap-font-major: var(--sap-font-family); /* @deprecated: use --sap-font-family */')
  lines.push('  --sap-font-minor: var(--sap-font-family); /* @deprecated: use --sap-font-family */')
  lines.push('')
  lines.push('  /* === Radius and shadow tokens (consumed by components) === */')
  lines.push('  --sap-radius-card: 0.5rem;')
  lines.push('  --sap-radius-button: 0.25rem;')
  lines.push('  --sap-shadow-card: 0 1px 4px rgba(0, 0, 0, 0.08);')
  lines.push('')
  lines.push('  /* === v0.2 deprecation aliases === */')
  for (const [oldName, newName] of Object.entries(V02_RENAME_MAP)) {
    lines.push(`  ${oldName}: var(${newName}); /* @deprecated: use ${newName} */`)
  }
  lines.push('}')
  lines.push('')
  return lines.join('\n')
}
